#!/usr/bin/env node
// Materialize `.env.local` from `.env.example` + the process environment.
//
// On VM startup, Cursor injects saved Secrets as environment variables. This
// script reads the KEY names declared in `.env.example`, then writes any that
// are present in `process.env` into `.env.local`. It only ever reads values
// from the environment and writes them to the file — it never prints secret
// values (the summary lists key NAMES only), so running it does not expose
// secrets to anyone reading the logs.
//
// Behaviour:
//   - No-op (exit 0) when `.env.example` is absent.
//   - Preserves any keys already present in an existing `.env.local`.
//   - For each key in `.env.example`, if a non-empty env var of that name
//     exists, its value is written/refreshed in `.env.local`.
//   - `.env.local` is git-ignored, so nothing here is ever committed.
//
// Usage: run from a directory containing `.env.example` (defaults to cwd).
//   node scripts/setup-env-local.mjs [--dir <path>] [--example <file>]

import fs from "node:fs";
import path from "node:path";

function parseArgs(argv) {
  const opts = { dir: process.cwd(), example: ".env.example", target: ".env.local" };
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--dir") opts.dir = argv[++i];
    else if (argv[i] === "--example") opts.example = argv[++i];
    else if (argv[i] === "--target") opts.target = argv[++i];
  }
  return opts;
}

// Pull `KEY` from lines like `KEY=...` (ignoring comments / blank lines).
function exampleKeys(text) {
  const keys = [];
  for (const raw of text.split(/\r?\n/)) {
    const line = raw.trim();
    if (!line || line.startsWith("#")) continue;
    const m = /^(?:export\s+)?([A-Za-z_][A-Za-z0-9_]*)\s*=/.exec(line);
    if (m) keys.push(m[1]);
  }
  return keys;
}

// Parse existing `KEY=VALUE` pairs so we don't clobber values already set.
function parseEnvFile(text) {
  const map = new Map();
  for (const raw of text.split(/\r?\n/)) {
    const line = raw.trim();
    if (!line || line.startsWith("#")) continue;
    const m = /^(?:export\s+)?([A-Za-z_][A-Za-z0-9_]*)\s*=(.*)$/.exec(line);
    if (m) map.set(m[1], m[2]);
  }
  return map;
}

// Quote values that would break dotenv parsers (spaces, #, quotes, newlines).
function formatEnvValue(value) {
  if (/^[A-Za-z0-9_./:+-@]+$/.test(value)) return value;
  return `"${value.replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\n/g, "\\n")}"`;
}

function main() {
  const opts = parseArgs(process.argv.slice(2));
  const examplePath = path.resolve(opts.dir, opts.example);
  const targetPath = path.resolve(opts.dir, opts.target);

  if (!fs.existsSync(examplePath)) {
    console.log(`setup-env-local: no ${opts.example} found — skipping.`);
    return;
  }

  const keys = exampleKeys(fs.readFileSync(examplePath, "utf8"));
  const existing = fs.existsSync(targetPath)
    ? parseEnvFile(fs.readFileSync(targetPath, "utf8"))
    : new Map();

  const merged = new Map(existing);
  const filled = [];
  for (const key of keys) {
    const value = process.env[key];
    if (value !== undefined && value !== "") {
      merged.set(key, value);
      filled.push(key);
    }
  }

  if (merged.size === 0) {
    console.log(
      `setup-env-local: no matching env vars set for ${keys.length} key(s) in ${opts.example} — nothing written.`,
    );
    return;
  }

  const header =
    "# Generated by scripts/setup-env-local.mjs from .env.example + environment.\n" +
    "# git-ignored — do not commit. Re-run the script to refresh from Secrets.\n";
  const body = [...merged.entries()]
    .map(([k, v]) => `${k}=${formatEnvValue(v)}`)
    .join("\n");
  fs.writeFileSync(targetPath, header + body + "\n");

  // Names only — never log the values.
  console.log(
    filled.length > 0
      ? `setup-env-local: wrote ${opts.target} with ${filled.length} key(s) from environment: ${filled.join(", ")}`
      : `setup-env-local: ${opts.target} left unchanged (no env-backed keys among ${keys.length} in ${opts.example}).`,
  );
}

main();
